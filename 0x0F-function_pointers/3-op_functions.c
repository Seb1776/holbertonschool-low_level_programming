#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - Sumate two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substract two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplicate two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Module of numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
